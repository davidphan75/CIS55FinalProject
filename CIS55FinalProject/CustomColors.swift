import UIKit

class CustomColors: UIColor {
    
    class func FPBackgroundGray()->UIColor{
        let color = UIColor(red: 230/255 , green: 230/255 , blue: 230/255 , alpha: 1)
        return color
    }
    
    class func FPTextGray()->UIColor{
        let color = UIColor(red: 128/255 , green: 128/255 , blue: 128/255 , alpha: 1)
        return color
    }
    
}
